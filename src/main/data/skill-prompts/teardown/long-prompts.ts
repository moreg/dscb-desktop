/* =========================================================
   Stage 6：文风.md（整书级写作技法视图）
   ========================================================= */

export function stage6Prompt(
  bookName: string,
  sampleChapters: string,
  teardownReport: string
): string {
  return `## 任务：Stage 6 文风分析

产出《${bookName}》的文风.md，整书级写作技法视图，供下游写作日更循环喂给写作模型。**总上限 ~4000 字**。

### 方法论要点
- **权威范围**：文风.md 只管句法层（句长/标点/对话潜台词/情绪节奏），情绪/节奏意图只引用节奏.md/情绪模块.md，不在此重定义。
- **每子项标可信度**（高/中/低），原文锚点片段标「出处 + 示范点」。
- **原文锚点**：4-6 段 × 300-500 字，按基调分类。

### 抽样章节（含原文锚点来源）
${sampleChapters}

### 拆文报告（引用情绪/节奏意图）
${teardownReport}

### 输出格式（严格按此骨架，总上限 ~4000 字）

\`\`\`markdown
# 文风：${bookName}

## 生成记录
参考了哪些资料 + 抽样看了哪几章 + 文风是否可用（<100字）

## 整体语感
句长分布 / 标点习惯 / 段落节奏（每子项标可信度，~500字）

## 对话技法
潜台词模式 / 对话标签 / 角色语气区分（潜台词必标可信度，~400字）

## 情绪交替模式
章内基调切换 / 跨章基调周期 / 喜剧↔重击转场（章内切换必标可信度，~400字）

## 可借鉴技巧
写法技巧 Top 5 + 可借鉴套路 Top 3（从拆文报告引用；只引用情绪模块.md/节奏.md，不重定义，~300字）

## 原文锚点片段
4-6 段 × 300-500 字，按基调分类，标"出处 + 示范点"（~2400字）

## 不可模仿
{对标书缺陷或不适合本项目的技法，<100字}
\`\`\`

只输出上述 markdown。`
}

