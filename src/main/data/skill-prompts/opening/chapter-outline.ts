/**
 * 开书 Phase 3（下）：首批细纲 prompt（源自 story-long-write Phase 3 细纲）。
 * 依据核心设定与大纲，生成首批细纲章节内容。
 */

export const FIRST_CHAPTERS_BATCH = 10

export function buildChapterOutlinePrompt(
  coreSettings: string,
  volumeOutline: string,
  fromChapter: number,
  count: number,
  chapterWordCount: number,
  isGolden: boolean
): string {
  const goldenRule = isGolden
    ? `
### 黄金三章法则（前 3 章决定留存，格外严格）
1. **开篇钩子**：第 1 章前 500 字必须有强钩子（悬念/冲突/反差/信息差/代入感）。
2. **主角立人设**：通过行为/对话展示主角特质，不要使用大段旁白直接介绍。
3. **世界观渗透**：通过事件自然带出设定，杜绝说明文式的科普。
4. **章末悬念**：每章末留钩子，第 3 章末要建立强烈的追读动力。
5. **爽点验证**：前 10 章至少包含 1 个完整的爽点循环（铺垫→释放→反应层）。`
    : ''

  const today = new Date().toISOString().slice(0, 10)

  return `## 任务：产出第 ${fromChapter}-${fromChapter + count - 1} 章的详细细纲

### 📌 核心设定
${coreSettings}

### 📊 卷级大纲
${volumeOutline}

### 📏 篇幅约束
- 每章字数：约 ${chapterWordCount} 字
${goldenRule}

### 📚 结构约束（来自当前《小说立项》技能）
- 默认真相源为按卷细纲：\`大纲/细纲_第XX卷.md\`
- 可额外按章输出：\`大纲/细纲_第XXX章.md\`
- 细纲内容必须与 \`设定/\`、\`追踪/\`、\`图解/节奏图谱.html\` 保持一致
- 如引入新角色/新地点/新势力，内容中要明确到足够让系统后续落盘到对应文件

### 📐 细纲模板（每章严格按此结构输出，包含所有必填字段，冒号使用中文冒号或英文冒号均可）

每一章在顶部用 \`=== 第X章 ===\` 分隔，内容如下：

=== 第{N}章 ===
**版本**：v1.0（${today} 创建）
**修改记录**：
- v1.0（${today}）：初版

## 第 {N} 章：{章节名}
- **节奏标注（必填，对齐节奏图谱）**：
  - 情绪值：{情绪值，1-10 整数}
  - 爽点类型：{爽点值，0/1/2/3/3.5/4}
- **核心事件**：{本章发生的最核心事件，一句话描述}
- **爽点/打脸**：{本章爽点/打脸内容；无显性爽点写"功能是..."}
- **角色出场**：{按出场顺序写出所有角色及路人，符合人设起名黑名单限制}
- **伏笔铺设/回收**：{本章埋设/回收的伏笔编号及内容，如：埋设 FB-001/回收 FB-002/无}
- **章末钩子**：{具体的悬念或事件，勾起下一章的追读动力}
- **字数预估**：约 ${chapterWordCount} 字
- **金句**：{可选，画龙点睛的一句话}

### 📖 情节细化（五段式起因-发展-转折-高潮-结尾，含字数预算密/疏点）
- **起因**【铺垫·密 ${Math.round(chapterWordCount * 0.15)}字】：{}
- **发展**【日常·疏 120字】：{}
- **转折**【信息揭示·疏 40字】：{}
- **高潮**【爽点/突破/激战·密 ${Math.round(chapterWordCount * 0.3)}字】：{}
- **结尾**【收尾/悬念·密 150字】：{}

---

### 💡 细纲生成限制与禁忌（必须遵守）
1. **章号格式**：必须使用阿拉伯数字，写为 \`## 第 1 章：章节名\`（使用 \`第 1 章\`，严禁写成 \`第一章\`）。
2. **AI味检测防屏蔽**：
   - 禁用典型 AI 转折和模板词（如：❌"眼神中闪过一丝冷意"、❌"嘴角勾起一抹不易察觉的弧度"、❌"然而，他并不知道..."、❌"这让XXX感到十分震惊"）。
   - 主角、配角、反派的对白必须体现其显性/隐性性格，有符合身份的口头禅或语气词。
3. **爽点真实性**：
   - 爽点逻辑链必须完整（挑衅→主角隐忍/触发条件→反杀/打脸→众人震惊反应→主角实际收益）。
   - 大高潮之后必须有 1-2 章的“消化期/铺垫期”，爽点强度为 0。
4. **格式检查项**（必须包含以下概念）：
   - 情节细化对应 **内容概括（五段式）**。
   - 各部分后面的字数标注对应 **字数预算**。
   - 各项预算求和对应 **预算合计**。
5. **追踪意识**：
   - 伏笔编号、角色状态变化、地点/势力首次出现，必须写得可追踪，方便后续同步到 \`追踪/伏笔.md\`、\`追踪/角色状态.md\`、\`设定/世界观/地理.md\`、\`设定/势力/\`。

输出 ${count} 章，每章用 \`=== 第{N}章 ===\` 分隔（{N} 从 ${fromChapter} 开始）。不要任何额外解释。`
}
