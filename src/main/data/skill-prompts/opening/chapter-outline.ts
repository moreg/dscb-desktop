/**
 * 开书 Phase 3（下）：首批细纲 prompt（源自 story-long-write Phase 3 细纲）。
 * 依据核心设定与大纲，生成首批细纲章节内容。
 */

import { TOMATO_CHAPTER_NAME_SPEC, STUCK_FLOW_HINT } from './shared-specs'

export const FIRST_CHAPTERS_BATCH = 10

export function buildChapterOutlinePrompt(
  coreSettings: string,
  volumeOutline: string,
  fromChapter: number,
  count: number,
  chapterWordCount: number,
  isGolden: boolean
): string {
  const goldenRule = isGolden
    ? `
### 黄金三章法则（前 3 章决定留存，格外严格）
1. **开篇钩子**：第 1 章前 500 字必须有强钩子（悬念/冲突/反差/信息差/代入感）。
2. **主角立人设**：通过行为/对话展示主角特质，不要使用大段旁白直接介绍。
3. **世界观渗透**：通过事件自然带出设定，杜绝说明文式的科普。
4. **章末悬念**：每章末留钩子，第 3 章末要建立强烈的追读动力。
5. **爽点验证**：前 10 章至少包含 1 个完整的爽点循环（铺垫→释放→反应层）。`
    : ''

  const today = new Date().toISOString().slice(0, 10)

  return `## 任务：产出第 ${fromChapter}-${fromChapter + count - 1} 章的详细细纲

### 📌 核心设定
${coreSettings}

### 📊 卷级大纲
${volumeOutline}

### 📏 篇幅约束
- 每章字数：约 ${chapterWordCount} 字
${goldenRule}

### 📚 结构约束（来自当前《小说立项》技能）
- 默认真相源为按卷细纲：\`大纲/细纲_第XX卷.md\`
- 逐章独立文件落盘路径：\`细纲/细纲_第NNN章_标题.md\`（NNN 三位零填充，标题须符合番茄章名风格）
- 细纲内容必须与 \`设定/\`、\`追踪/\`、\`图解/节奏图谱.html\` 保持一致
- 如引入新角色/新地点/新势力，内容中要明确到足够让系统后续落盘到对应文件

${TOMATO_CHAPTER_NAME_SPEC}

### 📐 细纲模板（每章严格按此结构输出，包含所有必填字段，冒号使用中文冒号或英文冒号均可）

每一章在顶部用 \`=== 第X章 ===\` 分隔，内容如下（必填子节缺一不可）：

=== 第{N}章 ===
**版本**：v1.0（${today} 创建）
**修改记录**：
- v1.0（${today}）：初版

## 第 {N} 章：{章节名}
> 章节名必须符合上方番茄章名风格规范，且与文件名 \`细纲_第${'{NNN}'}章_标题.md\` 的标题部分完全一致

- **节奏标注（必填，对齐节奏图谱）**：
  - 情绪值：{情绪值，1-10 整数}
  - 爽点类型：{爽点值，0/1/2/3/3.5/4}
- **核心事件**：{本章发生的最核心事件，一句话描述}
- **字数目标**：约 ${chapterWordCount} 字
- **目标情绪**：{紧张 → 爽 → 暧昧升温 等情绪变化关键词}
- **章首钩子**：{未完成动作开局 / 悬念开局 / 冲突开局等，具体场景描写}
- **本章爽点**：{本章爽点/打脸内容；无显性爽点写"功能是..."}
- **情绪变化曲线**：{紧张（发现被盯上）→ 警觉 → 冷笑 → 爽 → 暧昧余韵}
- **角色出场**：{按出场顺序写出所有角色及路人，符合人设起名黑名单限制}
- **伏笔铺设/回收**：{本章埋设/回收的伏笔编号及内容，如：埋设 FB-001/回收 FB-002/无}

## 内容概括（五段式）
- **起因**【铺垫·密 ${Math.round(chapterWordCount * 0.15)}字】：{承接前章，本章起因}
- **发展**【日常·疏 120字】：{具体动作链}
- **转折**【信息揭示·疏 40字】：{新信息出现，被迫行动}
- **高潮**【爽点/突破/激战·密 ${Math.round(chapterWordCount * 0.3)}字】：{动作+反应+在场配角差异化反应链}
- **结尾**【收尾/悬念·密 150字】：{具体钩子场景，留下未解决问题}

## 情节安排（多线）
- **主线推进**：{主角从 X 到 Y}
- **辅线推进**：{配角/反派线}
- **事件线/任务线**：{事件链}
- **感情线/关系线**：{关系变化}
- **逻辑线**：{因果链，下一章如何承接}

## 人物关系和出场顺序
- **出场顺序**：{角色A → 角色B → 角色C}
- **人物关系变化**：{A ↔ B：从 X → Y}
- **视角/信息差**：{谁知道什么、谁不知道什么}

## 情节细化
### 情节点序列
1. {情节点1【铺垫/信息揭示/转折/爽点/留钩】}
2. {情节点2}
...

### 代价兑现 / 收益兑现
- **主角代价**：{}
- **主角收益**：{}
- **反派代价**：{}

## 结尾设定和钩子
- **结尾设定**：{}
- **未解决问题**：1. {} 2. {}
- **下一章推动力**：{}
- **章尾钩子**：{具体动作 + 留下的问题}

## 写作禁区
1. {角色A不能直接承认喜欢B，保持人设}
2. {反派反应不能脸谱化}
3. {爽点必须显化"准备过程"}

## 章首/章尾钩子类型标注
- 章首：{未完成动作开局 / 悬念开局 / 冲突开局}
- 章尾：{暧昧+悬念 / 纯悬念 / 爽点余韵}

## 字数预算契约（情节点序列）
- 情节点 1-2：铺垫·疏，约 ${Math.round(chapterWordCount * 0.15)} 字
- 情节点 3：人物塑造·疏，约 150 字
- 情节点 5-6：转折·密，约 ${Math.round(chapterWordCount * 0.2)} 字
- 情节点 7-10：高潮·密，约 ${Math.round(chapterWordCount * 0.3)} 字
- 情节点 11-15：爽点余韵+结尾·密，约 ${Math.round(chapterWordCount * 0.2)} 字
- **预算合计**：约 ${chapterWordCount} 字

## 追踪关联
- **回收伏笔**：{FB-NNN 描述} 或 "无"
- **推进伏笔**：{FB-NNN 描述，回收百分比} 或 "无"
- **新埋伏笔**：{FB-NNN 描述，预计回收章节} 或 "无"
- **角色状态变更**：{角色名：实力/立场/目标的变更} 或 "无"

---

### 💡 细纲生成限制与禁忌（必须遵守）
1. **章号格式**：必须使用阿拉伯数字，写为 \`## 第 1 章：章节名\`（使用 \`第 1 章\`，严禁写成 \`第一章\`）。
2. **三处标题一致性**：文件名 \`细纲_第NNN章_标题.md\` 的标题 == 文件内 \`## 第 N 章：标题\` 的标题 == 大纲逐章节奏标注表的标题列。
3. **AI味检测防屏蔽**：
   - 禁用典型 AI 转折和模板词（如：❌"眼神中闪过一丝冷意"、❌"嘴角勾起一抹不易察觉的弧度"、❌"然而，他并不知道..."、❌"这让XXX感到十分震惊"）。
   - 主角、配角、反派的对白必须体现其显性/隐性性格，有符合身份的口头禅或语气词。
4. **爽点真实性**：
   - 爽点逻辑链必须完整（挑衅→主角隐忍/触发条件→反杀/打脸→众人震惊反应→主角实际收益）。
   - 大高潮之后必须有 1-2 章的"消化期/铺垫期"，爽点强度为 0。
5. **追踪意识**：
   - 伏笔编号、角色状态变化、地点/势力首次出现，必须写得可追踪，方便后续同步到 \`追踪/伏笔.md\`、\`追踪/角色状态.md\`、\`设定/世界观/地理.md\`、\`设定/势力/\`。

${STUCK_FLOW_HINT}

输出 ${count} 章，每章用 \`=== 第{N}章 ===\` 分隔（{N} 从 ${fromChapter} 开始）。不要任何额外解释。`
}
