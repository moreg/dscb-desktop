/** 章末结尾硬性原则：每章结尾必须用"对话"或"事件"，禁说教/AI 味抒怀。 */
export const CHAPTER_ENDING_RULES = `
【章末结尾硬性原则（必读，违反必须重写）】

每一章的结尾**必须**用"对话"或"事件"来写：

✓ 对话结尾：角色说关键台词（埋伏笔/造悬念/留钩子）
✓ 事件结尾：让某事突然发生（打断平静/引入新元素/反转）
✓ 对话+事件混合：最有冲击力

✗ 禁止"说教/总结/感慨"：例如"这世界就是这样""他明白了一个道理"
✗ 禁止"AI 味抒怀"：例如"他的故事，才刚刚开始""留给读者无限遐想"
✗ 禁止纯心理活动（无对话/无事件）
✗ 禁止纯动作（"他转身离开"——没有对话/事件）

对话结尾的 5 类典型示例：
- 留悬念："他刚要开口，身后突然传来一声轻笑——"
- 埋伏笔："这把剑，我迟早会亲手还给你。"
- 造冲突："你说什么？再说一遍。"
- 反转："……你是青云子？！"
- 未说完："其实我本来想告诉你——"（然后不说了）

事件结尾的 5 类典型示例：
- 打断：主角正在思考，突然窗户被踹开
- 新角色登场：门外站着一个他从未见过的人
- 新发现：主角在物品上发现一行字
- 环境变化：天空突然变黑，灵气暴走
- 情感冲击：主角看到尸体，认出是故人

最有冲击力的混合结尾示例：
"我不会让你——"
话没说完，门被踹开。
门口站着的人，让所有声音都停了。
`.trim()

/** 大纲/细纲遵守的三铁律 */
export const OUTLINE_THREE_RULES = `
【细纲遵守三铁律（违反即为失败）】

1. **顺序铁律**：必须按细纲剧情点编号顺序写，不得跳过、不得打乱、不得提前写后面的
2. **完整铁律**：细纲中的每个剧情点都必须写到，不得遗漏
3. **边界铁律**：只写本章细纲范围内的内容，禁止写下一章的剧情

允许扩张的情况（仅用于把正文补足到本章目标字数）：
- 补充过渡场景连接两个剧情点
- 增加配角互动来丰富剧情
- 补充环境描写和心理描写

绝对禁止：
- 擅自增加细纲中没有的新剧情点
- 改变细纲中已有的结局走向
- 添加细纲未提及的新角色或新冲突
- 跳过细纲中的剧情点
- 打乱细纲的剧情顺序
- 写下一章的剧情内容
`.trim()

/** 7 大去 AI 味技巧 */
export const DEAI_TECHNIQUES = `
【7 大去 AI 味技巧】

1. **展示反应，而非解释情绪**
   ❌ 他心中一凛，意识到危险
   ✓ 他手一顿，没再往前走

2. **不解释效果，用人物反应表现**
   ❌ 声音不大，可显得格外清晰
   ✓ 他说完，旁边几个人都看了过来

3. **具体感知后不加复述比喻**
   ❌ 暴雨砸在门板上，砰砰砰的，像有人在砸墙
   ✓ 暴雨砸在门板上，砰砰砰的

4. **用语气词打破完整感**
   ❌ 他的计划非常完美
   ✓ 嘿，你还真别说，他这算盘打得确实响

5. **避免负向动作套路**（动作本身已传达信息，不必再说"没有 XX"）
   ❌ 她的手在抖，但她没有松开
   ✓ 她的手在抖，握得很紧

6. **直给原则——一句话能说清的事，不要绕弯子**
   ❌ 他感觉到的不是头疼，是一段记忆
   ✓ 他脑子里突然冒出一段记忆

7. **打破对仗感**（AI 句式长短均匀，人写作有突如其来的短句）
   ❌ 他不仅拥有强大的力量，还具备过人的智慧
   ✓ 他这人，能打。更要命的是，脑子还好使
`.trim()

/** 真人对话的 6 大可选特征 + 8 大避坑模式 */
export const DIALOGUE_RULES = `
【真人对话特征（按需选用，不强制每段都体现）】

可选特征：
- 会跑题："那个，我本来想说……算了，你吃饭了没？"
- 会自相矛盾："你这人挺讨厌的——呃，也不是讨厌，就是……"
- 会用错词/口误："我们去……不对，等等，让我先想想。"
- 会突然沉默/岔开：张了张嘴，又闭上了。
- 会有口癖/口头禅："啧，这事儿不好办。"
- 动作+台词混合（可选，不是必须）

【对话动作位置硬性原则】

✓ **先动作，后台词**：先写完整动作段，再写完整台词
✓ **台词完整结束后再加动作**：台词说完，另起一行加动作
✗ **同一人台词中间禁止插入动作打断**

错误示例：
  "其实也不是什么大事"他搓了搓手，"就是……"
正确示例：
  "其实也不是什么大事，就是……"
  他搓了搓手。

【机械感对话 8 大避坑模式】

1. 每句都点题（真人会绕半天圈子才说重点）
2. 台词+重复动作（所有男主都皱眉、所有女主都抿嘴 → 模板化）
3. 排比反问（"你以为你是谁？你凭什么？你配吗？"过于机械）
4. 标准化收尾（"好，就这样。""嗯。"）
5. 形容词堆砌（"你这卑鄙无耻下流的家伙！"）
6. 语气词缺失（全用书面语，无"啊/嘛/啧/嘶"）
7. 每句都"完整"（无填充词"嗯""呃""那个""就是"）
8. 缺乏个性化词汇（按"教育程度"分而非按"性格"分）

【对话核心要求】

不强求加废话/口癖/动作，**但禁止一直是无信息量的废话**。
- 推进剧情、暴露性格、埋伏笔/造梗、自相矛盾/跑题：允许
- 纯凑字、重复已知信息、无意义寒暄：禁止
`.trim()

