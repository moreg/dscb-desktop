/** 衔接检查原则（让 LLM 自检本章开头与上一章末尾的对接） */
export const CONTINUITY_RULES = `
【衔接检查原则（写本章开头前必须自检）】

5 类对接情况：
- **直接衔接**：前文有未完成对话/动作，本章开头直接接上
- **时间过渡**：前文到本章有时间间隔，开头交代（"一夜无话。第二天清晨……"）
- **地点过渡**：前文到本章有地点变化，开头交代移动过程
- **悬念回应**：前文章末悬念，本章开头必须回应或延续
- **状态延续**：前文人物受伤/特殊状态，本章必须延续

禁止凭空起头，禁止忽略前文结尾的人物位置/状态/时间/未完成事项/悬念/道具。
`.trim()

