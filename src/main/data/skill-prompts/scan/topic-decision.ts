/**
 * 构建选题决策 prompt（Phase 4）。
 * 产出 2-3 个推荐选题，按选题四步结构化。
 */
export function buildTopicDecisionPrompt(report: string, platform: string): string {
  return `## 任务：分析榜单 + 产出选题决策

基于下面的扫榜报告，做市场分析并产出 2-3 个推荐选题。

### 分析维度

1. **题材热度排行**：哪些题材/流派重复出现（跨样本信号）？
2. **新题材信号**：是否有新兴卖点/金手指？
3. **经典题材动态**：传统题材（玄幻/都市/言情）当前是什么状态？
4. **新元素提取**：热门作品共性的新元素（设定/人设/切入点）？
5. **关键数据洞察**：字数分布、更新频率、标签聚类等。

### 选题四步（每个选题都要走完）

1. **能爆的原因**（假设，标 \`待拆文验证\`）：为什么这个选题能爆？对应哪类读者需求？
2. **市场验证**：榜单里有几本同方向？数据如何？（同方向样本 <15 时可行性强制降级）
3. **差异化定位**：与现有热门作品的区别在哪？不能纯跟风换皮。
4. **可行性 + 失败风险 + 验证动作**：可行性（高/中/低）+ 主要风险 + 下一步验证（拆哪本对标书/试写黄金三章）。

### 硬规则

- 可行性上限：数据稀疏（\`[数据稀疏]\` 或样本 <15）⇒ 不许给"高"，强制"中" + 写明先验证；内置知识模式一律"中"。
- 不输出无法支撑的题材；不只看热度；不忽略平台调性。
- "能爆的原因"是假设，明确标注 \`待拆文验证\`。

### 输出格式（严格按此 markdown 骨架）

\`\`\`markdown
# 选题决策：${platform}

## 市场分析

### 题材热度排行
（跨样本重复的题材/流派，按信号强度排序）

### 新题材信号
（新兴卖点/金手指，标注样本数）

### 经典题材动态
（传统题材当前状态）

### 关键数据洞察
（字数/更新/标签聚类）

## 推荐选题

### 选题一：{题材 + 核心卖点}

- **能爆的原因**（待拆文验证）：...
- **市场验证**：榜单同方向 N 本，数据...
- **差异化定位**：与 X 作品的区别...
- **可行性**：高/中/低
- **失败风险**：...
- **验证动作**：拆《对标书》/ 试写黄金三章 / ...

### 选题二：...

### 选题三：...

## 下一步建议

开书时把本文件放到小说项目根目录，写作流程会自动读取"能爆的原因"。
想坐实假设，先拆对标书（拆文库功能）。
\`\`\`

### 扫榜报告
${report}

只输出上述 markdown。`
}

/** 构建内置知识模式的选题 prompt（采集失败降级用，明确标注假设） */
export function buildBuiltinTopicPrompt(
  builtinKnowledge: string,
  platform: string,
  isLong: boolean
): string {
  return `${buildTopicDecisionPrompt('', platform)}

> ⚠️ **降级模式：本次未采集到实时榜单数据，以下为内置题材趋势知识。所有可行性一律标"中"，"能爆的原因"为假设。**

### 内置题材趋势（候选假设，非实时数据）
${builtinKnowledge}

### 篇幅
${isLong ? '长篇（网文连载）' : '短篇（盐言/七猫短篇等）'}

基于内置知识产出 2-3 个候选选题方向，全部标注为假设。`
}
